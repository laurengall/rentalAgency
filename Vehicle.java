class Vehicle {
  private String make; 
  private String model; 
  private int year; 
  private int numWheels; 

  public Vehicle(){
    this.make = make; 
    this.model = model;
    this.year = year;
    this.numWheels = numWheels;
  }
  public Vehicle(String ma, String mo, int y, int nW){
    make = ma; 
    model = mo;
    year = y; 
    numWheels = nW; 
  }
}