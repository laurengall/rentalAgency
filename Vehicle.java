public class Vehicle {
  private String make;
  private String model;
  private int year;
  private int numWheels;
  private String color;

  public Vehicle() {
    this.make = make;
    this.model = model;
    this.year = year;
    this.numWheels = numWheels;
    this.color = color;
  }

  public Vehicle(String ma, String mo, int y, int nW, String c) {
    make = ma;
    model = mo;
    year = y;
    numWheels = nW;
    color = c;
  }
}